package clueGame;

public class Solution {
	public String room;
	public String weapon;
	public String person;
	
	public Solution(String room, String weapon, String person) {
		this.room = room;
		this.weapon = weapon;
		this.person = person;
	}

	public String getRoom() {
		return room;
	}

	public String getWeapon() {
		return weapon;
	}

	public String getPerson() {
		return person;
	}
	
	
	
	
	
}
